#include <system.h>

char *exception_messages[];

void* isr_routines[32];
void isr_handler(struct Registers* regs)
{
    if (regs->int_no < 32)
    {
        void (*handler)(struct Registers* regs);
        handler = isr_routines[regs->int_no];
        if (handler)
            handler(regs);
        else
        {
            puts(" Exception Occurred:\n\t");
            puts(exception_messages[regs->int_no]);
            puts("\n\n");
        }
    }
}

void isr_install_handler(int isr, void (*handler)(struct Registers* regs))
{
    isr_routines[isr] = handler;
}

void isr_uninstall_handler(int isr)
{
    isr_routines[isr] = 0;
}

